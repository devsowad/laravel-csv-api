     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $rows = Sales::where('batch_id', $id)
            ->get('body');

        foreach ($rows as $k => $r) {
            $rows[$k] = json_decode($r->body);
        }

        $filename = UserBatch::where('batch_id', $id)->get('file_name')->first()->file_name;

        return ['rows' => $rows, 'file_name' => $filename];
    }

